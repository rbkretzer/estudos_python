"""
Python = Linguagem de programação
Tipo de tipagem = Dinâmica / Forte
str -> string -> texto
Strings são textos que estão dentro de aspas
"""

# Aspas simples
print('Rafael Kretzer')
print(1, 'Rafael "Kretzer"')

# Aspas duplas
print("Rafael Kretzer")
print(2, "Rafael 'Kretzer'")

# Escape
print('Rafael \'Kretzer\'')

# r
print(r'Rafael \'Kretzer\'')